import Foundation

@objcMembers
class EnrollmentPoints : NSObject, NSCoding, EmbeddedObject {

    var achieved: NSNumber?
    var maximal: NSNumber?
    var percentage: NSNumber?

    required init(_ dict: [String : AnyObject]) {
        achieved = dict["achieved"] as? NSNumber
        maximal = dict["maximal"] as? NSNumber
        percentage = dict["percentage"] as? NSNumber
    }

    required init(coder decoder: NSCoder) {
        achieved = decoder.decodeObject(forKey: "achieved") as? NSNumber
        maximal = decoder.decodeObject(forKey: "maximal") as? NSNumber
        percentage = decoder.decodeObject(forKey: "percentage") as? NSNumber
    }

    func encode(with coder: NSCoder) {
        coder.encode(achieved, forKey: "achieved")
        coder.encode(maximal, forKey: "maximal")
        coder.encode(percentage, forKey: "percentage")
    }
    
}
