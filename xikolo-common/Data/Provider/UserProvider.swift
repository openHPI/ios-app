import BrightFutures
import Foundation
import Spine

class UserProvider {

    class func getMe() -> Future<UserSpine, XikoloError> {
        var query = Query(resourceType: UserSpine.self, path: "/api/v2/users/me")
        query.include("profile")
        return SpineHelper.findOne(query)
    }

}
