import BrightFutures
import Foundation

class LTIExerciseProvider {

    class func getLTIExercise(_ ltiExerciseId: String) -> Future<LTIExerciseSpine, XikoloError> {
        return SpineHelper.findOne(ltiExerciseId, ofType: LTIExerciseSpine.self)
    }

}
